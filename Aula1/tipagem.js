let variavel = 18;//number
console.log(typeof variavel);

variavel = 'Nick Lopes';//string
console.log(typeof variavel);

variavel = true;//boolean
console.log(typeof variavel);

variavel = {nome:'Nick Lopes', idade:18};//object
console.log(typeof variavel);

variavel = [1,2,3,4,5];//object
console.log(typeof variavel);

variavel = function() {return 1}//função
console.log(typeof variavel);

variavel = undefined;//indefinido
console.log(variavel);

variavel = null;//nulo-object
console.log(variavel);

variavel = NaN;//Not a Number - "contas erradas 0/0"-number
console.log(variavel);